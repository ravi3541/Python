# Modify the tuple

tup1 = (34,87,[32,10],37,[85,83],'Hello')
print("Original Tuple before Modification : ",tup1)


tup1[2][0] = 3200
print("\n After tup1[2][0] = 3200 : ",tup1)

tup1[4][0] = [30,50]
print("\n tup1[4][0] = [30,50] : ",tup1)

tup1[4][0][1] = 13030
print("\n tup1[4][0][1] = 13030 : ",tup1)

